package main

import (
	"fmt"

	"github.com/signintech/gopdf"
)

func tp(millimeters float64) float64 {
	// 1 inch == 72 typographic points; 1 inch == 25.4 millimeters.
	return millimeters / 25.4 * 72
}

func main() {
	pdf := gopdf.GoPdf{}
	pdf.Start(gopdf.Config{
		PageSize: gopdf.Rect{W: tp(210), H: tp(297)}, // A4, portrait
	})
	pdf.AddPage()
	err := pdf.AddTTFFont("NotoSans", "assets/ttf/NotoSans-Regular.ttf")
	if err != nil {
		fmt.Println(err.Error())
		return
	}
	err = pdf.SetFont("NotoSans", "", 14)
	if err != nil {
		fmt.Println(err.Error())
		return
	}
	pdf.Cell(nil, "PDF file generated by Invoicer")
	pdf.WritePdf("out.pdf")
	fmt.Printf("Calculated page size in tp: %.2f by %.2f\n", tp(210), tp(297))
}
